def get_video_url(video_id=None):
    if video_id is None:
        return
    query_url = f'https://www.iesdouyin.com/web/api/v2/aweme/iteminfo/?item_ids={video_id}'
    try:
        logger.info(f'【查询视频信息】：url为{query_url}')
        response = requests.get(query_url, headers=get_headers(), timeout=10)
    except Exception as e:
        logger.error(f'【查询视频信息】：{e}')
        return

    if response.status_code == 200:
        result = response.json()
        if result is None:
            logger.error(f'【查询视频信息】请求返回数据为空，video_id：{video_id}')
        else:
            if result.get('status_code') != 0:
                logger.error(f'【查询视频信息】请求返回status_code不为0，video_id：{video_id}')
                return
            item_list = result.get('item_list')
            if item_list is None or len(item_list) == 0:
                logger.error(f'【查询视频信息】请求返回数据中item_list为空，video_id：{video_id}')
                return
            try:
                video_url = item_list[0]['video']['play_addr']['url_list'][0]
                video_url = video_url.replace('playwm', 'play')
                video_url = video_url.replace('720p', '1080p')
            except Exception as e:
                logger.error(f'【查询视频信息】：{e}')
                return
            logger.info(f'【查询视频信息】：视频源地址为{video_url}')
            return video_url


def get_headers():
    return dict({
        'User-Agent': random.choice(USER_AGENTS),
        'accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,image/apng,*/*;q=0.8,application/signed-exchange;v=b3',
        'accept-encoding': 'gzip, deflate',
        'accept-language': 'zh-CN,zh;q=0.9',
        'cache-control': 'no-cache',
        'pragma': 'no-cache',
        'sec-fetch-mode': 'navigate',
        'sec-fetch-site': 'same-site',
        'sec-fetch-user': '?1',
        'upgrade-insecure-requests': '1',
    })


def main(input_str):
    search_result = re.search(r'https.*/', input_str)
    if search_result is None:
        logger.error(f'输入的链接有误：{input_str}')
        return None
    else:
        input_short_url = search_result.group()
        input_long_url = requests.head(input_short_url).headers.get('location')
        temp_url = input_long_url.split('?').pop(0)
        if temp_url.endswith('/'):
            temp_url = temp_url[:-1]
        video_id = temp_url.split('/').pop()
        return get_video_url(video_id)
